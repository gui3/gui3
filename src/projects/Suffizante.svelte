

<h2>
  Suffizante (Work In Progress)
</h2>

<p>
  Ce travail en cours vise à développer un petit logiciel Open Source
  pour aider à la création de
  <a href="https://sfzformat.com/">
    bibliothèques de samples musicaux en .sfz
  </a>
  lisibles par exemple par le sampleur
  <a href="https://www.plogue.com/francais/produits/sforzando-fr.html">
    Sforzando
  </a>,
  également Open Source.
  <br/>
  Cette idée m'est venue car le leader du marché des sampleurs,
  <strong>Kontakt par Native Instruments</strong>,
  ne m'inspire vraiment pas et que les concurrents Open Source
  manquent cruellement de bibliothèques de samples, 
  de par le fait qu'il faut les créer à la main.
</p>

<a href="https://github.com/gui3/Suffizante">
  Suivez l'avancement du projet ICI ou essayez le logiciel chez vous
</a>

<h3>
  Réalisé à ce jour
</h3>

<p>
  Projet commencé le 25/04/2021
  <br/>
  J'ai établi la structure du logiciel grâce au framework Electron,
  j'arrive à lire un fichier audio, ainsi qu'afficher un graphe
  représentant la forme d'onde de manière convaincante quelle que soit l'échelle.
  On peut zoomer et se déplacer sur le graphe. Voici un apercu:
